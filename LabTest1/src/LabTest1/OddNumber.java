//1.WAP to print all odd numbers between 1 to 20.

package LabTest1;

public class OddNumber {

	public static void main(String[] args) {
		
		System.out.println("----Odd number between 1 to 20----");
		//This loop add 2 everytime and skip even numbers.
		for(int i=1;i<=20;i+=2)
		{
			System.out.print(i+" ");
		}

	}

}
